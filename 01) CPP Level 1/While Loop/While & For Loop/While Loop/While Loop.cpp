#include <iostream>
using namespace std;

int main()
{
    cout << "  For Loop  \n";
    // for loop from 1 to 5
    for (int i = 1; i <= 5; i++)
    {
        cout << i << endl;
    }

    cout << "\n  While Loop  \n";
    // while loop from 1 to 5
    int i = 1;
    while (i <= 5)
    {
        cout << i << endl;
        i++;
    }

    return 0;
}
